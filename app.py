def calculer_puissance(nombre, puissance):
    """
    Cette fonction calcule la puissance d'un nombre donné.

    Args:
        nombre (float): Le nombre à calculer.
        puissance (int): La puissance à laquelle élever le nombre.

    Returns:
        float: Le résultat du calcul de la puissance.
    """
    resultat = nombre ** puissance
    return resultat
