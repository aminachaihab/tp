"""Module pour calculer la puissance d'un nombre"""

def calculer_puissance(nombre, puissance):
    """Calcule la puissance d'un nombre.

    Args:
        nombre (float ou int): Le nombre à élever à la puissance.
        puissance (float ou int): La puissance à laquelle élever le nombre.

    Returns:
        float ou int: Le résultat de la puissance.
    """
    return nombre ** puissance
